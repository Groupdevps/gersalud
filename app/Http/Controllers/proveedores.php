    {
        $newproveedor = new proveedor();
        $newproveedor->nit=$request->nit;
        $newproveedor->Razon_social=$request->datos[0]['value'];
        $newproveedor->direccion=$request->datos[1]['value'];
        $newproveedor->Userd_id=$request->datos[2]['value'];
        $newproveedor->tipo_pro=$request->datos[3]['value'];
        $newproveedor->save();
    }

